def xor_block(data: bytes, key: bytes) -> bytes:
    if not data:
        return data
    result = bytearray(data)
    key_int = struct.unpack('<I', key)[0]
    aligned = len(result) & ~3
    for i in range(0, aligned, 4):
        val = struct.unpack_from('<I', result, i)[0]
        struct.pack_into('<I', result, i, val ^ key_int)
    for i in range(aligned, len(result)):
        result[i] ^= key[i & 3]
    return bytes(result)


def halfwidth_to_fullwidth(text: str) -> str:
    result = []
    for ch in text:
        if ch in Encoding.HALFWIDTH_TO_FULLWIDTH:
            result.append(Encoding.HALFWIDTH_TO_FULLWIDTH[ch])
        else:
            result.append(ch)
    return ''.join(result)


def encode_text_for_game(text: str, target_encoding: str = 'gbk',
                         source_encoding: str = 'shift_jis') -> bytes:
    if target_encoding == 'shift_jis':
        return text.encode('shift_jis', errors='replace')

    if target_encoding == 'utf-8':
        return text.encode('utf-8')

    for old, new in Encoding.SJIS_TO_GBK_REPLACE.items():
        text = text.replace(old, new)

    placeholders = {}
    for i, ch in enumerate(Encoding.SJIS_KEEP_CHARS):
        placeholder = f'\x01KEEP{i}\x01'
        placeholders[placeholder] = ch
        text = text.replace(ch, placeholder)

    encoded = text.encode('gbk', errors='replace')

    for placeholder, ch in placeholders.items():
        encoded = encoded.replace(
            placeholder.encode('gbk', errors='replace'),
            ch.encode('shift_jis', errors='replace')
        )

    return encoded


def detect_text_encoding(data: bytes) -> str:
    if data[:3] == b'\xef\xbb\xbf':
        return Encoding.UTF8

    for enc in [Encoding.UTF8, Encoding.GBK, Encoding.SJIS]:
        try:
            data.decode(enc)
            return enc
        except (UnicodeDecodeError, ValueError):
            continue

    return Encoding.SJIS
